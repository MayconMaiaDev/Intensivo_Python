# Tipo de dado bool (boolean)
# Ao questinar algo em um programa,
# Só existem duas respostas poissiveis:
# sim, verdadeiro (true) ou não, falso (false).
# Existem vários operadores para "questinar".
# Dentre eles o ==, que é um operador lógico que
# questina se um valor é igual a outro
print(10 == 10) #sim -. True (verdadeiro)
print(10 == 11) # não -. False (falso)
print(type(True))
print(type(False))
print(type(10 == 10))
print(type(10 == 11))